function Single_Show_Results(Time,Y)
% % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % 
% % Function to plot the results
% % Called by : Main Program
% % Calls : none
% % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % %

% % Define global variables
global NPTS TBULK

Y = Y';

x = 0 : 1/(NPTS-1) : 1;

% i0 = find(Time >= 5*60 , 1);
i1 = find(Time >= 15*60 , 1);
i2 = find(Time >= 30*60 , 1);
i3 = find(Time >= 60*60 , 1);
i4 = find(Time >= 90*60 , 1);

figure
hold on
plot(x,Y(1:NPTS,i1),'LineWidth',2);
plot(x,Y(1:NPTS,i2),'g','LineWidth',2);
plot(x,Y(1:NPTS,i3),'r','LineWidth',2);
plot(x,Y(1:NPTS,i4),'k','LineWidth',2);
annotation('textarrow',[0.8 0.8],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i1)/60)) ' min']},...
    'LineWidth',2,...
    'Color',[0 0 1]);
annotation('textarrow',[0.6 0.6],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i2)/60)) ' min']},...
    'LineWidth',2,...
    'Color',[0 1 0]);
annotation('textarrow',[0.4 0.4],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i3)/60)) ' min']},...
    'LineWidth',2,...
    'Color',[1 0 0]);
annotation('textarrow',[0.2 0.2],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i4)/60)) ' min']},...
    'LineWidth',2);
axis([0 1 0 1])
box on
xlabel('Radial Position','FontSize',12)
ylabel({'Dimensionless';'C=C Concentration'},'FontSize',12)
title(['Predicted for Courtaulds SAF Precursor at ',num2str(TBULK-273),...
    ' Centigrade'],'FontSize',12)
hold off

figure
hold on
plot(x,Y(NPTS+1:2*NPTS,i1),'LineWidth',2);
plot(x,Y(NPTS+1:2*NPTS,i2),'g','LineWidth',2);
plot(x,Y(NPTS+1:2*NPTS,i3),'r','LineWidth',2);
plot(x,Y(NPTS+1:2*NPTS,i4),'k','LineWidth',2);
annotation('textarrow',[0.8 0.8],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i1)/60)) ' min']},...
    'LineWidth',2,...
    'Color',[0 0 1]);
annotation('textarrow',[0.6 0.6],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i2)/60)) ' min']},...
    'LineWidth',2,...
    'Color',[0 1 0]);
annotation('textarrow',[0.4 0.4],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i3)/60)) ' min']},...
    'LineWidth',2,...
    'Color',[1 0 0]);
annotation('textarrow',[0.2 0.2],...
    [0.6 0.45],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{[num2str(round(Time(i4)/60)) ' min']},...
    'LineWidth',2);
axis([0 1 0 1])
box on
xlabel('Radial Position','FontSize',12)
ylabel({'Dimensionless Bound';'Oxygen Concentration'},'FontSize',12)
title(['Predicted for Courtaulds SAF Precursor at ',...
    num2str(TBULK-273),' Centigrade'],'FontSize',12)
hold off

beep
beep