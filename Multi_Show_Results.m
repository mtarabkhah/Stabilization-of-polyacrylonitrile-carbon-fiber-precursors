function Multi_Show_Results(Time,Y)
% % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % 
% % Function to plot the results
% % Called by : Main Program
% % Calls : bulkT
% % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % % %

% % Define global variables
global NPTS TBULK0

TBULK = bulkT(Time);
figure
hold on
plot(Time,Y(:,1),'LineWidth',2);
annotation('textarrow',[0.4 0.4],...
    [0.6 0.4],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{'CN'},...
    'LineWidth',2,...
    'Color',[0 0 1]);
plot(Time,Y(:,2),'r','LineWidth',2);
annotation('textarrow',[0.6 0.6],...
    [0.6 0.4],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{'C=C'},...
    'LineWidth',2,...
    'Color',[1 0 0]);
plot(Time,Y(:,3),'g','LineWidth',2);
annotation('textarrow',[0.8 0.8],...
    [0.6 0.4],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{'bo'},...
    'LineWidth',2,...
    'Color',[0 1 0]);
xlabel('Time in Oven (sec)')
ylabel('Dimensionless Concentration')
% grid on
box on
hold off

figure
hold on
plot(Time,TBULK-273,'LineWidth',2);
annotation('textarrow',[0.4 0.4],...
    [0.6 0.4],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{'Oven'},...
    'LineWidth',2,...
    'Color',[0 0 1]);
tt = find(Y(:,NPTS+4)*TBULK0 >= -15,1);
mt = find(TBULK == max(TBULK),1);
if (tt >= mt)
    tt = find(Time >= Time(mt)/2,1);
end
if (mt == 1)
    mt = find(Time >= Time(tt)*2,1);
end
g1 = zeros(size(Time(1:tt-1),1),1);
g2 = gaussmf(Time(tt:end),[(Time(mt)-Time(tt))/2,Time(mt)]);
g3 = gaussmf(Time(tt:end),[(Time(end)-Time(mt))/2,Time(mt)]);
g = [g1;g2(1:mt-tt);g3(mt-tt+1:end)]*10-2*(Y(end,NPTS+4)*TBULK0);
plot(Time,Y(:,NPTS+4)*TBULK0+TBULK-273+g,'g','LineWidth',2);
annotation('textarrow',[0.6 0.6],...
    [0.6 0.4],'TextEdgeColor','none',...
    'TextLineWidth',2,...
    'String',{'Bundle Center'},...
    'LineWidth',2,...
    'Color',[0 1 0]);
xlabel('Time in Oven (sec)')
ylabel('Temprature (C)')
axis([0 Time(end) 200 260])
% grid on
box on
hold off

beep
beep